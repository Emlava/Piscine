int	ft_sqrt(int nb)
{
	int	x;

	x = 0;
	if (nb >= 500000000)
		x = 22360;
	if (nb >= 750000000)
		x = 27386;
	if (nb >= 1000000000)
		x = 31622;
	if (nb >= 1250000000)
		x = 35355;
	if (nb >= 1500000000)
		x = 38729;
	if (nb >= 1750000000)
		x = 41833;
	if (nb >= 2050000000)
		x = 44721;
	if (nb >= 2147483000)
		return (0);
	while (x * x < nb)
		x++;
	if (x * x == nb)
		return (x);
	else
		return (0);
}

